public class Book {

    private String name;  
    private String author; 

    // Set name
    public void setName(String n) { 
        name = n;
    }

    // Set author
    public void setAuthor(String a) { 
        author = a;
    }

    // Get name
    public String getName() {
        return name; 
    }

    // Get author
    public String getAuthor() {
        return author; 
    }
}
